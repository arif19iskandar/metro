<script>
// variable
var navbarHeight = 0;

// function init
function init(){
    navbarHeight = $('#navbar').outerHeight()
    $('body').attr('data-offset', navbarHeight)
    navbar_scroll_animate()
    new WOW().init();

    $('img.svg').each(function(){
        var $img = jQuery(this);
        var imgID = $img.attr('id');
        var imgClass = $img.attr('class');
        var imgURL = $img.attr('src');

        jQuery.get(imgURL, function(data) {
            // Get the SVG tag, ignore the rest
            var $svg = jQuery(data).find('svg');

            // Add replaced image's ID to the new SVG
            if(typeof imgID !== 'undefined') {
                $svg = $svg.attr('id', imgID);
            }
            // Add replaced image's classes to the new SVG
            if(typeof imgClass !== 'undefined') {
                $svg = $svg.attr('class', imgClass+' replaced-svg');
            }

            // Remove any invalid XML tags as per http://validator.w3.org
            $svg = $svg.removeAttr('xmlns:a');

            // Check if the viewport is set, if the viewport is not set the SVG wont't scale.
            if(!$svg.attr('viewBox') && $svg.attr('height') && $svg.attr('width')) {
                $svg.attr('viewBox', '0 0 ' + $svg.attr('height') + ' ' + $svg.attr('width'))
            }

            // Replace image with new SVG
            $img.replaceWith($svg);

        }, 'xml');
    })
}

// function navbar scroll animate
function navbar_scroll_animate() {
    if($(document).scrollTop() > navbarHeight) {
        $('.navbar').addClass('navbar-active navbar-light bg-white shadow-sm')
        $('.navbar').removeClass('navbar-dark bg-transparent')
    } else {
        $('.navbar').addClass('navbar-dark bg-transparent')
        $('.navbar').removeClass('navbar-active  navbar-light bg-white shadow-sm')
    }
}


// on document ready
$(document).ready(function(){
    // initial javascript
    init();
    
    // hide collapse on click
    $('#navbarNav .nav-link').click(function(){
        $('.collapse').collapse('hide')
    });
    
    // on scroll document
    $(document).scroll(function(){
        navbarHeight = $('#navbar').outerHeight()
        $('body').attr('data-offset', navbarHeight)
        navbar_scroll_animate();}
    )

    // on link smooth scroll
    $('a[href*="#"].smooth').not('[href="#"]').not('[href="#0"]').click(function(event){
        if (location.pathname.replace(/^\//, '') == this.pathname.replace(/^\//, '') &&  location.hostname == this.hostname ) {
            var target = $(this.hash);
            target = target.length ? target : $('[name=' + this.hash.slice(1) + ']');
            if (target.length) {
                event.preventDefault();
                $('html, body').animate({
                    scrollTop: target.offset().top - navbarHeight > 0 ? target.offset().top - navbarHeight : target.offset().top
                }, 1000)
            }
        }
    })
})
